package com.nanosl.helpmate.donation;

import java.io.Serializable;
import java.util.Date;

public class Donation implements Serializable {

    private String id;
    private String uid;
    private String username;
    private String photoUrl;
    private String description;
    private String contactPerson;
    private String address;
    private String city;
    private String phone;
    private String gpsLocation;
    private String status;
    private Date addedTime;

    public Donation() {
    }

    public Donation(String description, String contactPerson, String address, String city, String phone, String gpsLocation, String uid, String username, String photoUrl) {
        this.description = description;
        this.contactPerson = contactPerson;
        this.address = address;
        this.city = city;
        this.setPhone(phone);
        this.setGpsLocation(gpsLocation);
        this.setUsername(username);
        this.setPhotoUrl(photoUrl);
        this.status = "active";
        this.setUid(uid);
        this.setAddedTime(new Date());
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getContactPerson() {
        return contactPerson;
    }

    public void setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getGpsLocation() {
        return gpsLocation;
    }

    public void setGpsLocation(String gpsLocation) {
        this.gpsLocation = gpsLocation;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }

    public void setPhotoUrl(String photoUrl) {
        this.photoUrl = photoUrl;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public Date getAddedTime() {
        return addedTime;
    }

    public void setAddedTime(Date addedTime) {
        this.addedTime = addedTime;
    }

}
